const month = 4
switch (month) {
  case 1:
    console.log('Jan');
    break;
  case 2:
    console.log('feb');
    break;

  case 3:
    console.log('march');
    break;

  case 4:
    console.log('april');
    break;

}


const year = 2005
switch (year) {
  case 2000:
    console.log('Year 2001');
    break;
  case 2001:
    console.log('Year 2002');
    break;

  case 2002:
    console.log('Year 2002');
    break;

  case 2003:
    console.log('Year 2003');
    break;

  case 2004:
    console.log('Year 2004');
    break;

  case 2005:
    console.log('Year 2005');
    break;
  default:
    console.log('no match');
}